#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*temp;

	temp = s1;
	while (*temp != '\0')
	{
		++temp;
	}
	while (*s2 != '\0')
	{
		*temp = *s2;
		++temp;
		++s2;
	}
	*temp = '\0';
	return (s1);
}
